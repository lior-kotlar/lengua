// @ts-check
/**
 * Emit runtime schema constants that `openapi-typescript` cannot express in the TS types.
 *
 * `openapi-typescript` turns the OpenAPI schema into TypeScript *types*, which are erased at
 * runtime and cannot carry numeric constraints such as an array's `maxItems`. A few of those
 * constraints are needed by the web app at runtime (e.g. the per-request word cap on
 * `POST /generate`, surfaced so the Generate form can warn the user before the server 422s).
 *
 * Rather than hard-code those numbers in the web app (where they would silently drift from the
 * backend contract), this script extracts them from the committed `apps/api/openapi.json` and
 * writes them into `src/constants.ts`. It runs as part of `pnpm gen:api` right after the type
 * generation, and a CI drift check fails the PR if the committed output is stale — so the
 * constants always match the contract that produced `schema.ts`.
 *
 * The output is deterministic (stable key order, LF line endings, trailing newline) so the drift
 * check is byte-for-byte reproducible across platforms.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const openapiPath = resolve(here, '../../../apps/api/openapi.json');
const outputPath = resolve(here, '../src/constants.ts');

/** Read a deeply-nested value, throwing a clear error if any segment is missing. */
function requirePath(root, segments) {
  let node = root;
  for (const segment of segments) {
    if (node === null || typeof node !== 'object' || !(segment in node)) {
      throw new Error(
        `openapi.json is missing the expected path: ${segments.join('.')} ` +
          `(failed at "${segment}"). Did the backend contract change?`,
      );
    }
    node = node[segment];
  }
  return node;
}

function requireNumber(root, segments) {
  const value = requirePath(root, segments);
  if (typeof value !== 'number' || !Number.isFinite(value)) {
    throw new Error(
      `Expected a number at ${segments.join('.')}, got ${JSON.stringify(value)}.`,
    );
  }
  return value;
}

const openapi = JSON.parse(readFileSync(openapiPath, 'utf8'));

const generateWordsMaxItems = requireNumber(openapi, [
  'components',
  'schemas',
  'GenerateRequest',
  'properties',
  'words',
  'maxItems',
]);

const lines = [
  '/**',
  ' * Runtime schema constants extracted from apps/api/openapi.json.',
  ' *',
  ' * AUTO-GENERATED by scripts/generate-constants.mjs (run via `pnpm gen:api`) — DO NOT EDIT.',
  ' * These mirror numeric OpenAPI constraints (e.g. array `maxItems`) that the generated',
  ' * TypeScript types in `schema.ts` cannot carry at runtime. A CI drift check fails the PR if',
  ' * this file is stale, so the values always match the committed backend contract.',
  ' */',
  '',
  '/** Numeric limits the API enforces, surfaced for client-side validation. */',
  'export const schemaLimits = {',
  '  /**',
  '   * Maximum number of words accepted by `POST /generate` in a single request',
  '   * (`GenerateRequest.words.maxItems`). The server rejects more with HTTP 422.',
  '   */',
  `  generateWordsMaxItems: ${generateWordsMaxItems},`,
  '} as const;',
  '',
];

writeFileSync(outputPath, lines.join('\n'), 'utf8');

console.log(
  `Wrote ${outputPath} (generateWordsMaxItems=${generateWordsMaxItems}).`,
);
